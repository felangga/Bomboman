    public void CreateConnection(final String namaUser) {

        ListRoom = new Thread(new Runnable() {
            @Override
            public void run() {
                try {
                    System.out.println("Client running...");
                    InetAddress multicastAddress = InetAddress.getByName("224.0.0.2");
                    lblIPAddress.setText(InetAddress.getLocalHost().getHostAddress());
                    int multicastPort = 9693;

                    MulticastSocket ms = new MulticastSocket(multicastPort);
                    ms.joinGroup(multicastAddress);

                    String ip;
                    String message;

                    while (client_online) {
                        byte[] buffer = new byte[8192];
                        DatagramPacket dp = new DatagramPacket(buffer, buffer.length);
                        ms.receive(dp);
                        message = new String(buffer, 0, buffer.length);
                        System.out.println("Received Message : " + message.trim());
                        String cut[] = message.trim().split("-");

                        if (cut.length > 3) {
                            if (!daftarOrang.contains(cut[0] + "-" + cut[3])) {
                                daftarOrang.add(cut[0] + "-" + cut[3]);
                            }
                        }
                        ip = (cut[0]);
                        if (ip.contains("%%")) {
                            listServer.clear();
                            daftarOrang.clear();
                        } else if (ip.contains(".")) {
                            String ls = cut[1] + " (" + cut[0] + ")";
                            if (!listServer.contains(ls)) {
                                listServer.addElement(ls);
                            }
                        }

                    }
                    System.out.println("Client Killed");
                } catch (UnknownHostException ex) {
                    Logger.getLogger(Client.class.getName()).log(Level.SEVERE, null, ex);
                } catch (IOException ex) {
                    Logger.getLogger(Client.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        });
        ListRoom.start();

    }

