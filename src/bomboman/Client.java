    private void lstServerMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lstServerMouseClicked
        listOrang.removeAllElements();
        for (int i = 0; i < daftarOrang.size(); i++) {
            String ip = (String) lstServer.getSelectedValue();
            // cek apakah string tidak kosong
            if (ip != null && ip.length() > 2) {
                String getIP = ip.substring(ip.indexOf("(") + 1, ip.indexOf(")")).trim();
                if (daftarOrang.get(i).contains(getIP)) {
                    String potong[] = daftarOrang.get(i).split("-");
                    String pisah[] = potong[1].split(":");
                    for (int j = 0; j < pisah.length; j++) {
                        listOrang.addElement(pisah[j]);
                    }
                }
            }
        }



    }//GEN-LAST:event_lstServerMouseClicked

